<?php

namespace App\Models\Extension;
  
use Illuminate\Database\Eloquent\Factories\HasFactory;
use App\Models\BaseModel;
use GoldSpecDigital\LaravelEloquentUUID\Database\Eloquent\Uuid;
use App\Models\Traits\Relationships\ExtensionOfficerLanguageRelationship;
  
class ExtensionOfficerLanguage extends BaseModel
{
    use Uuid, ExtensionOfficerLanguageRelationship;
  
    protected $fillable = [
        'extension_officer_id',
        'language_id', 
    ];


    /**
     * every time a model is created
     * automatically assign a UUID to it
     *
     * @var array
     */
    protected static function boot()
    {
        parent::boot();
        self::creating(function (ExtensionOfficerLanguage $model) {
            $model->id = $model->generateUuid();
        });
    }

    /**
     * The "type" of the auto-incrementing ID.
     *
     * @var string
     */
    protected $keyType = 'string';

    /**
     * Indicates if the IDs are auto-incrementing.
     *
     * @var bool
     */
    public $incrementing = false;
}
